export class Maze {
    constructor() {
        this.data = JSON.parse(JSON.stringify(MAZE_DATA));
        this.dotsRemaining = this.countDots();
        this.totalDots = this.dotsRemaining;
    }

    getTile(x, y) {
        if (x < 0 || x >= MAZE_WIDTH || y < 0 || y >= MAZE_HEIGHT) {
            return TILE_TYPE.WALL;
        }
        return this.data[Math.floor(y)][Math.floor(x)];
    }

    setTile(x, y, value) {
        if (x >= 0 && x < MAZE_WIDTH && y >= 0 && y < MAZE_HEIGHT) {
            this.data[Math.floor(y)][Math.floor(x)] = value;
        }
    }

    isWall(x, y) {
        return this.getTile(x, y) === TILE_TYPE.WALL;
    }

    isGhostGate(x, y) {
        return this.getTile(x, y) === TILE_TYPE.GHOST_GATE;
    }

    isTunnel(x, y) {
        return this.getTile(x, y) === TILE_TYPE.TUNNEL;
    }

    countDots() {
        let count = 0;
        for (let y = 0; y < MAZE_HEIGHT; y++) {
            for (let x = 0; x < MAZE_WIDTH; x++) {
                const tile = this.data[y][x];
                if (tile === TILE_TYPE.DOT || tile === TILE_TYPE.POWER_PELLET) {
                    count++;
                }
            }
        }
        return count;
    }

    reset() {
        this.data = JSON.parse(JSON.stringify(MAZE_DATA));
        this.dotsRemaining = this.countDots();
    }
}
